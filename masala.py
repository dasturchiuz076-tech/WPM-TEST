x = int(input("1-sonni kiriting: "))
y = int(input("2-sonni kiriting: "))
amal = input("Amalni kiriting: (+, -, *, ^, /, %): ")
if amal == "+":
    print(x + y)
elif amal == "-":
    print(x - y)
elif amal == "*":
    print(x * y)
elif amal == "^":
    print(x ** y)
elif amal == "/":
    print(x / y)
elif amal == "%":
    print(x % y)
